

package switchstmt;


public class Switchstmt {

   
    public static void main(String[] args) {
       
        int age=25;
        
        switch (age){
            
            case 23:
                
                System.out.println("Your age is 23");
                break;
                
            case 22:
                
                System.out.println("Your age is 22");
                break;  
                
                
            case 25:
                
                System.out.println("Your age is 25");
                break; 
                
                
            default:
          System.out.println(age);
                    
        }
    }
    
}
